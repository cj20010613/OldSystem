    public void memo_save(View view) {
        SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");//设置日期格式
        String time = "" + df.format(new Date());
        String contant = memo_contant.getText().toString();
        if(contant.equals("") || contant.equals(null)) {
            Toast.makeText(MemoActivity.this,"备忘信息不能为空，请输入备忘信息！！！",Toast.LENGTH_SHORT).show();
        } else {
            List<Memo> memos = DataSupport.findAll(Memo.class);
            Boolean judge = false;
            for(Memo memo: memos) {
                if(memo.getContant().equals(contant)) {
                    judge = true;
                    break;
                }
            }
            if(judge) {
                Toast.makeText(MemoActivity.this,"该备忘信息已保存，不需要重复保存！！！",Toast.LENGTH_SHORT).show();
            } else {
                Memo memo = new Memo();
                memo.setContant(contant);
                memo.setStoreTime(time);
                memo.save();
                Toast.makeText(MemoActivity.this,"备忘信息保存成功！！！",Toast.LENGTH_SHORT).show();
            }

        }

    }

    public void memo_queryAll(View view) {
        List<Memo> memos =  DataSupport.findAll(Memo.class);
        String temp = "";
        for (Memo memo: memos) {
            temp += "备忘编号："+memo.getId()+"\n备忘内容："+memo.getContant()+"\n"+"备忘时间："+memo.getStoreTime()+"\n\n\n";
        }
        if(temp == "")
            temp = "当前未保存任何备忘信息，无备忘信息！！！";
        Intent intent = new Intent(MemoActivity.this, MemoInfoActivity.class);
        intent.putExtra("memo_info", temp);
        startActivity(intent);
        finish();
    }

    public static String getTime(Date date) {
        SimpleDateFormat format = new SimpleDateFormat("HH:mm");
        return format.format(date);
    }

    @Override
    public void onClick(View view) {
        switch (view.getId()) {
            case R.id.repeat_rl:
                selectRemindCycle();
                break;
            case R.id.ring_rl:
                selectRingWay();
                break;
            case R.id.set_btn:
                setClock();
                break;
            default:
                break;
        }
    }

    private void setClock() {
        if (time != null && time.length() > 0) {
            String[] times = time.split(":");
            if (cycle == 0) {//是每天的闹钟
                AlarmManagerUtil.setAlarm(this, 0, Integer.parseInt(times[0]), Integer.parseInt
                        (times[1]), 0, 0, "亲，备忘录设定提醒时间到啦，请注意查看哦", ring);
            } if(cycle == -1){//是只响一次的闹钟
                AlarmManagerUtil.setAlarm(this, 1, Integer.parseInt(times[0]), Integer.parseInt
                        (times[1]), 0, 0, "亲，备忘录设定提醒时间到啦，请注意查看哦", ring);
            }else {//多选，周几的闹钟
                String weeksStr = parseRepeat(cycle, 1);
                String[] weeks = weeksStr.split(",");
                for (int i = 0; i < weeks.length; i++) {
                    AlarmManagerUtil.setAlarm(this, 2, Integer.parseInt(times[0]), Integer
                            .parseInt(times[1]), i, Integer.parseInt(weeks[i]), "亲，备忘录设定提醒时间到啦，请注意查看哦", ring);
                }
            }
            Toast.makeText(this, "提醒闹钟设置成功", Toast.LENGTH_LONG).show();
        }

    }


